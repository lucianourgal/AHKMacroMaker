    public void consumirEspacosBrancosNoFinal(int a) {

        int t;
        boolean g = true;

        while (g) {
            t = S[a][0].length();

            if (S[a][0].charAt(t - 1) != ' ') {
                g = false;
            } else {
                S[a][0] = S[a][0].substring(0, t - 2);
            }
        }

    }

